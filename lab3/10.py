# 1. Класс-РОДИТЕЛЬ (Базовый класс)
# Это "общий" шаблон для любого человека. У всех людей есть имя.
class Person:
    def __init__(self, name):
        # Сохраняем имя в "карман" объекта
        self.name = name

# 2. Класс-РЕБЕНОК (Дочерний класс)
# В скобках пишем Person, чтобы Student "впитал" в себя всё от родителя.
class Student(Person):
    def __init__(self, name, gpa):
        # МАГИЯ super(): мы говорим Питону: 
        # "Сходи к родителю (Person) и попроси его выполнить его __init__".
        # Так нам не нужно заново писать self.name = name.
        super().__init__(name)
        
        # А вот GPA — это уже личная фишка студента, у обычного Person его нет.
        # Сохраняем его отдельно.
        self.gpa = gpa

    # Метод для вывода информации
    def display(self):
        # Используем f-строку. self.name пришел от родителя, а self.gpa — свой.
        print(f"Student: {self.name}, GPA: {self.gpa}")

# --- РАБОТА С ДАННЫМИ ---

# Считываем строку (например: "Aisha 3.8") и режем её по пробелу
data = input().split()

# Забираем имя и оценку из списка
name = data[0]
gpa = data[1]

# Создаем объект класса Student. 
# В этот момент запускается __init__ внутри Student, который вызывает __init__ у Person.
s = Student(name, gpa)

# Вызываем метод показа данных
s.display()