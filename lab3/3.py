import re # Подключаем регулярные выражения, чтобы легко найти знак (+, -, *, /)

def solve():
    # 1. Считываем строку и убираем лишние пробелы по бокам
    s = input().strip()
    
    # 2. Словари-переводчики (Lookup Tables)
    # Этот переводит слова в строковые цифры
    word_to_digit = {
        'ONE': '1', 'TWO': '2', 'THR': '3', 'FOU': '4', 'FIV': '5',
        'SIX': '6', 'SEV': '7', 'EIG': '8', 'NIN': '9', 'ZER': '0'
    }
    # Этот делает ровно наоборот: цифру превращает в слово
    # Мы создаем его автоматически на основе первого словаря
    digit_to_word = {v: k for k, v in word_to_digit.items()}

    # 3. Функция ДЕКОДИРОВАНИЯ (Слова -> Число)
    def decode(triplets):
        digits = ""
        # range(start, stop, step) — идем от 0 до конца строки шагом в 3 символа
        # Потому что каждое "число" (ONE, TWO) занимает ровно 3 буквы
        for i in range(0, len(triplets), 3):
            # Берем кусочек из 3 букв: triplets[0:3], потом triplets[3:6] и т.д.
            piece = triplets[i:i+3]
            # Находим цифру в словаре и приклеиваем к строке digits
            digits += word_to_digit[piece]
        return int(digits) # В конце превращаем строку "12" в число 12

    # 4. Функция КОДИРОВАНИЯ (Число -> Слова)
    def encode(number):
        if number == 0:
            return "ZER"
        res = ""
        # Превращаем число в строку "123", чтобы идти по каждой цифре отдельно
        for d in str(number):
            # Для каждой цифры ('1', '2'...) находим её слово ('ONE', 'TWO'...)
            res += digit_to_word[d]
        return res

    # 5. Поиск знака операции
    # Регулярка ищет любой символ из списка [ + , - , / , * ]
    match = re.search(r'([+-/*])', s)
    op = match.group(1) # Запоминаем, какой знак нашли (например, '+')
    
    # 6. Разрезаем строку по знаку операции на левую и правую части
    parts = s.split(op) # Если было "ONE+TWO", станет ["ONE", "TWO"]
    
    # Превращаем обе части в обычные числа через нашу функцию decode
    val1 = decode(parts[0])
    val2 = decode(parts[1])

    # 7. Выполняем саму математику
    if op == '+':
        result = val1 + val2
    elif op == '-':
        result = val1 - val2
    elif op == '*':
        result = val1 * val2
    else: 
        # Целочисленное деление (//), как обычно в таких задачах
        result = val1 // val2

    # 8. Переводим результат обратно в слова и печатаем
    print(encode(result))

# Запускаем программу
solve()