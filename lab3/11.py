# 1. Создаем КЛАСС (Чертеж "Пары чисел")
class Pair:
    # 2. КОНСТРУКТОР
    # При создании пары мы сразу передаем два числа и сохраняем их как a и b.
    def __init__(self, a, b):
        self.a = a
        self.b = b

    # 3. МЕТОД add (Функция сложения с другой парой)
    # Это самое интересное место!
    # self — это первая пара (та, что слева от точки: p1)
    # other — это вторая пара, которую мы передали в скобках (p2)
    def add(self, other):
        # Мы складываем "своё" число 'a' с числом 'a' другой пары
        a_sum = self.a + other.a
        
        # Мы складываем "своё" число 'b' с числом 'b' другой пары
        b_sum = self.b + other.b
        
        # Возвращаем готовую строку с результатом
        return f"Result: {a_sum} {b_sum}"

# --- РАБОТА ПРОГРАММЫ ---

# 4. Считываем данные. 
# На вход прилетает строка (например: "10 20 5 5")
# map(int, ...) превращает их в числа, а list() делает из них список.
data = list(map(int, input().split()))

# 5. Создаем два объекта (две пары)
# Из списка data берем первые два числа для первой пары
p1 = Pair(data[0], data[1])
# Берем следующие два числа для второй пары
p2 = Pair(data[2], data[3])

# 6. Вызываем метод сложения
# Мы просим пару p1 добавить к себе пару p2.
# Внутри метода: p1 станет словом 'self', а p2 станет словом 'other'.
print(p1.add(p2))