# 1. Создаем КЛАСС (Чертеж)
# Мы описываем, что должен уметь любой "обработчик строк"
class StringHandler:
    
    # 2. КОНСТРУКТОР (__init__)
    # Эта функция запускается автоматически, когда мы создаем объект.
    # Она подготавливает "место" для хранения данных.
    def __init__(self):
        # self.s — это "личная" переменная нашего будущего объекта.
        # Пока объект только создан, строка внутри него пустая.
        self.s = ""

    # 3. МЕТОД getString (Функция получения данных)
    # Параметр self говорит Питону: "запиши строку именно в ТОТ объект, 
    # для которого вызвали этот метод".
    def getString(self):
        # Считываем текст с клавиатуры и сохраняем его в self.s
        self.s = input()

    # 4. МЕТОД printString (Функция вывода данных)
    def printString(self):
        # Берем строку из self.s, делаем все буквы заглавными (.upper())
        # и выводим на экран.
        print(self.s.upper())

# --- РАБОТА С ОБЪЕКТОМ ---

# 5. Создаем ОБЪЕКТ (Экземпляр класса)
# Теперь 'handler' — это реальный "работник", созданный по чертежу StringHandler.
handler = StringHandler()

# 6. Просим работника считать строку
handler.getString()

# 7. Просим работника напечатать строку капсом
handler.printString()